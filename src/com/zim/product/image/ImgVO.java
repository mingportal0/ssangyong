package com.zim.product.image;

/**
 * @author sist
 *
 */
public class ImgVO {
	private String ORG_FILE_NM;
	private String EXT_NM;
	
	public ImgVO(){
		
	}
	
	public ImgVO(String oRG_FILE_NM, String eXT_NM) {
		super();
		ORG_FILE_NM = oRG_FILE_NM;
		EXT_NM = eXT_NM;
	}
	

	@Override
	public String toString() {
		return "ImgVO [ORG_FILE_NM=" + ORG_FILE_NM + ", EXT_NM=" + EXT_NM + "]";
	}

	public String getORG_FILE_NM() {
		return ORG_FILE_NM;
	}
	public void setORG_FILE_NM(String oRG_FILE_NM) {
		ORG_FILE_NM = oRG_FILE_NM;
	}
	public String getEXT_NM() {
		return EXT_NM;
	}
	public void setEXT_NM(String eXT_NM) {
		EXT_NM = eXT_NM;
	}
	
}
